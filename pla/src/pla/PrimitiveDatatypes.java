package pla;

public class PrimitiveDatatypes {
	
	public static void main(String[] args) {
		
		byte a = 0;
		short b = 0;
		long c = 0;
		int d = 0;
		float e = 0;
		double f = 0;
		char g = 0;
		
		System.out.println("byte a: " + a + 
						   "\nshort b: " + b + 
						   "\nlong c: " + c + 
						   "\nint d: " + d +
						   "\nfloat e: " + e +
						   "\ndouble f :" + f +
						   "\nchar g: " + g);

	}

}
